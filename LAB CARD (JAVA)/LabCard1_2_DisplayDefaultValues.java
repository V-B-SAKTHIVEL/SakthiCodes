package com.example.codeoftheday;

public class LabCard1_2_DisplayDefaultValues
{
    public static void main(String[] args)
    {
        int intType = 2;
        double doubleType = 5.4345;
        char charType = 'a';
        String StringType = "Hello world...";
        boolean booleantype = true;

        System.out.println("The default values of all primitive types: ");
        System.out.println("int datatype : " + intType);
        System.out.println("double datatype : " + doubleType);
        System.out.println("char datatype : " + charType);
        System.out.println("string datatype : " + doubleType);
        System.out.println("boolean datatype : " + booleantype);
    }
}
